<script>
import ImageList from './ImageList/ImageList'
import Zoom from './Zoom/Zoom'
import { mapGetters } from 'vuex'

export default {
  name: 'MyDetail',
  data() {
    return {
      skuNum: 1,
      timer: null
    }
  },
  components: {
    ImageList,
    Zoom
  },
  computed: {
    ...mapGetters('detail', ['skuInfo', 'spuSaleAttrList', 'categoryView']),
    goodsDesc() {
      const descArr = (this.skuInfo.skuDesc || '').split('\n')
      const arr = []
      for (let i = 0; i < descArr.length - 1; i = i + 2) {
        arr.push([descArr[i], descArr[i + 1]])
      }
      return arr
    }
  },
  methods: {
    changeSaleAttr(spuSaleAttrValueList, saleAttr) {
      // console.log(spuSaleAttrValueList, saleAttr)  接收渲染上去的一行数组的所有选择的属性对象组成的数组，比如红色 黑色 接收当前点击的这个元素的属性对象
      // 排它操作
      spuSaleAttrValueList.forEach((item) => {
        item.isChecked = '0' // 这里修改的是渲染的时候传递给html的数组的值，vuex仓库中的数据并没有被修改
        // 难道正确操作不是去仓库里面修改vuex中的值？
      })
      saleAttr.isChecked = '1'
    },
    addGoodsCart() {
      // 通知vuex发送ajax请求
      // 这里除了携带id和数量之外还要携带颜色内存大小等信息
      let resu = this.$store.dispatch('detail/addGoodsCart', { skuId: this.skuInfo.id, skuNum: this.skuNum })

      // 1. 请求成功了,携带参数路由跳转
      // 2. 请求失败了,提示用户
      // async修饰的函数返回的是一个promise，promise的状态根据返回值来决定，具体的代码决定
      resu
        .then((data) => {
          if (data) {
            // 跳转到加入购物车成功显示的那个组件
            this.$router.push({ path: '/addGoodsCartSuccess', query: { skuNum: this.skuNum } })
            // 其余的参数存储在sessionStorage里面
            // 这里键名使用一个就行了，因为每次加入购物车显示的这个页面只需要显示一条数据就行
            sessionStorage.setItem('SKUINFO', JSON.stringify(this.skuInfo))
          }
        })
        .catch((err) => {
          alert(`${err}, 加入购物车失败，请重试`)
        })
    }
  },
  watch: {
    skuNum() {
      // 拿一个值保存当前的this.skuNum，如果校验失败了，那还可以用这个有效的this.skuNum，回溯功能
      // 1. 如果是整数就不需要校验
      if (Number.isInteger(this.skuNum)) {
        this.oldSkuNum = this.skuNum
        return
      }
      // 2. 相当于设置了一个防抖器，连续修改的时候不会去校验是否合理，只有停止修改500ms之后才去校验是否合理并修改对应的值
      window.clearTimeout(this.timer)
      this.timer = setTimeout(() => {
        // 2.1 如果这个数字不是数字就返回上一次的值
        if (isNaN(this.skuNum * 1)) {
          this.skuNum = this.oldSkuNum
          return
        }
        // 2.2 说明这个数字是一个数字，那么就返回取整以后的值
        // 2.3 取整以后是个负数，那就等于1
        this.skuNum = Number.parseInt(this.skuNum) > 0 ? Number.parseInt(this.skuNum) : this.oldSkuNum
      }, 500)
    }
  },
  mounted() {
    // 通知vuex发送消息，获取商品的详细信息
    // console.log(this.$route.params.skuid)
    // console.log(this.$store.state.detail.test)
    // 获取的商品是通过路由的params参数传递过来的
    this.$store.dispatch('detail/getGoodsDetail', this.$route.params.skuid)
  }
}
</script>

